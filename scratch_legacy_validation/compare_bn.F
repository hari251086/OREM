c     ============================================================
c     compare_bn.F — Ground-truth validation harness (issue #12)
c     -------------------------------------------------------
c     Runs orem_run on each legacy KSROP-DataPrint zone's own raw
c     TLE file (data-driven from manifest.txt, generated by
c     ../../.../scratch/extract_legacy_bn.py) and dumps e_opt/bn_opt
c     per zone to orem_results.csv, for comparison against the
c     original 2017-2021 GA fits (gene9.txt / genesis.dat / Non-Opt
c     KS baseline) recorded in legacy_ground_truth.csv.
c
c     BN search bounds are deliberately widened to [10,300] here
c     (vs test_reentry.F's [80,160]) so this comparison isn't
c     pre-biased by the exact range issue #12 is questioning.
c
c     Compile:
c       ifx /heap-arrays /F:16777216 scratch_legacy_validation\compare_bn.F
c           orem.F rsm.F ga.F tle_evolution.F zone_select.F
c           ksrop\propagate_ks.F ksrop\Subrouts.F
c           ksrop\TLEread.F ksrop\Legendre.F
c           /exe:scratch_legacy_validation\compare_bn.exe
c     Run (from OREM repo root, so relative paths resolve):
c       scratch_legacy_validation\compare_bn.exe
c     -------------------------------------------------------
      program compare_bn

      implicit double precision (a-h, o-z)
      common /xy/ pi, d2r, r2d, amue, AU, R_Earth

      parameter(ndim_a = 291, mxz = 4)

      double precision ALT_a(ndim_a), DEN_a(ndim_a), SCH_a(ndim_a)
      integer natm

      double precision reentry(mxz), e_opt(mxz), bn_opt(mxz)
      double precision rms_o(mxz), zepoch(mxz), rpe(mxz)
      double precision t_obs(6), t_mean, t_std
      integer nzu, ierr, nzv
      integer zstat(mxz)

      character*10 norad_str, zlabel
      character*100 relpath, tle_file
      integer norad, nrows

      call init_constants()
      call read_atm_v(ALT_a, DEN_a, SCH_a, ndim_a, natm)

      open(unit=21, file='scratch_legacy_validation/manifest.txt',
     &     status='old')
      open(unit=22, file='scratch_legacy_validation/orem_results.csv',
     &     status='unknown')
      write(22,'(A)')
     &   'norad,zone_label,nzu,e_opt,bn_opt,rms,zstat,ierr'

      t_obs(1) = 0.d0
      t_obs(2) = 0.d0
      t_obs(3) = 0.d0
      t_obs(4) = 0.d0
      t_obs(5) = 0.d0
      t_obs(6) = 0.d0

      nrows = 0
 10   continue
      read(21,*,end=90) norad_str, zlabel, relpath
      nrows = nrows + 1
      read(norad_str,'(I10)') norad
      tle_file = 'scratch_legacy_validation/' // trim(relpath)

      write(*,'(A,A,A,A)') '  --- ', trim(norad_str), ' ', trim(zlabel)

      call orem_run(
     &   trim(tle_file), norad,
     &   t_obs, mxz, 8, 10.d0, 0.90d0, -1.d0,
     &   10.d0, 300.d0, 0,
     &   4, 200, 40, 16, 0.8d0, 0.01d0, 0.123d0,
     &   2, 0, 0,
     &   7.2921150d-5, 3.35281066d-3, 1.d0,
     &   0.d0, 0.d0, 0, 0,
     &   0.d0, 0.d0, 0.d0,
     &   ALT_a, DEN_a, SCH_a, natm,
     &   reentry, e_opt, bn_opt, rms_o,
     &   zepoch, nzu,
     &   zstat, nzv,
     &   rpe, t_mean, t_std, ierr)

      if (nzu .ge. 1) then
         write(22,'(A,A,A,A,I2,A,F10.6,A,F10.4,A,F10.4,A,I2,A,I2)')
     &      trim(norad_str), ',', trim(zlabel), ',', nzu, ',',
     &      e_opt(1), ',', bn_opt(1), ',', rms_o(1), ',',
     &      zstat(1), ',', ierr
      else
         write(22,'(A,A,A,A,I2,A,I2)')
     &      trim(norad_str), ',', trim(zlabel), ',', nzu, ',,,,,', ierr
      end if

      goto 10
 90   continue
      close(21)
      close(22)

      write(*,'(A,I3,A)') 'Processed ', nrows, ' zones.'

      stop
      end

c     ============================================================
c     ATM reader (copied from test_reentry.F)
c     ============================================================
      subroutine read_atm_v(alt, den, sch, maxn, natm)
      implicit double precision (a-h, o-z)
      integer maxn, natm
      double precision alt(maxn), den(maxn), sch(maxn)

      natm = 291
      if (natm .gt. maxn) natm = maxn
      alt(1) = 60.d0
      do i = 2, 141
         alt(i) = alt(i-1) + 1.d0
      end do
      do i = 142, natm
         alt(i) = alt(i-1) + 2.d0
      end do

      open(unit=88, file='input/ATM.DAT', status='old',
     &     err=900)
      read(88,110) (sch(i), i=1,61)
      read(88,110) (sch(i), i=62,natm)
      read(88,111) (den(i), i=1,61)
      read(88,111) (den(i), i=62,natm)
      close(88)
 110  format(8f10.3)
 111  format(8e10.3)

      do i = 1, natm
         alt(i) = alt(i) * 1000.d0
         sch(i) = sch(i) * 1000.d0
         den(i) = den(i) * 1.0d10
      end do
      return
 900  natm = 0
      return
      end
